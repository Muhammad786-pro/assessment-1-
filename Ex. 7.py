# count from 0 to 50
for i in range(51):
    print(i)

# count from 50 to 0
for i in range(50, -1, -1):
    print(i)

# count from 30 to 50
for i in range(30, 51):
    print(i)

# count from 50 to 10 in decrements of 2
for i in range(50, 9, -2):
    print(i)

# count from 100 to 200 in increments of 5
for i in range(100, 201, 5):
    print(i)
